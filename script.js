const languageMeta = {
  en: { lang: "en", dir: "ltr", title: "Shexa Portfolio" },
  ku: { lang: "ku", dir: "rtl", title: "پۆرتفۆلیۆی شێخا" }
};

const languageButtons = document.querySelectorAll("[data-lang]");
const translatableElements = document.querySelectorAll("[data-i18n]");

function setLanguage(language) {
  const selectedLanguage = translations[language] ? language : "en";
  const meta = languageMeta[selectedLanguage];

  document.documentElement.lang = meta.lang;
  document.documentElement.dir = meta.dir;
  document.body.dir = meta.dir;
  document.title = meta.title;

  translatableElements.forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = translations[selectedLanguage][key] || translations.en[key] || "";
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === selectedLanguage;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("preferred-language", selectedLanguage);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

const savedLanguage = localStorage.getItem("preferred-language");
const browserLanguage = navigator.language.toLowerCase().startsWith("ku") ? "ku" : "en";
setLanguage(savedLanguage || browserLanguage);
